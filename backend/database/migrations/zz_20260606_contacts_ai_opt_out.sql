-- Исключение контактов из ИИ-автоответов (opt-out). Например сам администратор/
-- сотрудник, который пишет через мессенджер для теста или координации — бот ему
-- НЕ отвечает. Проверяется в processAiTurn (единая точка всех путей хода бота).
-- Идемпотентно. БД общая для dev/prod => действует сразу на обоих.
ALTER TABLE contacts
  ADD COLUMN IF NOT EXISTS ai_agent_opt_out boolean DEFAULT false;

COMMENT ON COLUMN contacts.ai_agent_opt_out IS
  'Контакт исключён из ИИ-автоответов (бот не отвечает). Напр. администратор/сотрудник.';
